package com.securosys.tee.dto.tsb.response;

import io.swagger.v3.oas.annotations.media.Schema;
import jakarta.validation.constraints.NotEmpty;
import jakarta.validation.constraints.NotNull;
import lombok.Data;

@Data
@Schema(description = "Response containing the encrypted payload of the approvers onboarding.")
public class ApproverOnboardResponseDto {

    @NotEmpty
    @NotNull
    @Schema(description = "The encrypted payload containing the approvers p12 container and certificate")
    private String encryptedPayload;

    @NotNull
    @NotEmpty
    @Schema(description = "The initialization Vector used to decrypt the approvers p12 container")
    private String initializationVector;

}
